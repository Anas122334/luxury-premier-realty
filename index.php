    window.addEventListener('DOMContentLoaded', (event) => {
        var firstNameInput = document.getElementById("first_name");
        var lastNameInput = document.getElementById("last_name");
        var emailInput = document.getElementById("email");
        var phoneInput = document.getElementById("phone_number");

        var nameRegex = /^[A-Za-z]+$/;
        var emailRegex = /^\S+@\S+\.\S+$/;
        var phoneRegex = /^\+\d{1,}$/;

        firstNameInput.addEventListener('input', function() {
            if (!nameRegex.test(this.value)) {
                this.setCustomValidity("Please enter only alphabets in the First Name field.");
            } else {
                this.setCustomValidity("");
            }
        });

        lastNameInput.addEventListener('input', function() {
            if (!nameRegex.test(this.value)) {
                this.setCustomValidity("Please enter only alphabets in the Last Name field.");
            } else {
                this.setCustomValidity("");
            }
        });

        emailInput.addEventListener('input', function() {
            if (!emailRegex.test(this.value)) {
                this.setCustomValidity("Please enter a valid email address.");
            } else {
                this.setCustomValidity("");
            }
        });

        phoneInput.addEventListener('input', function() {
            if (!phoneRegex.test(this.value)) {
                this.setCustomValidity("Please enter a valid phone number in the format '+19492361225'.");
            } else {
                this.setCustomValidity("");
            }
        });
    });
